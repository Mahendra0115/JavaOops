// Type of Parametar 

 class TypeOfPara {
    void display(Integer x){
        System.out.println(x);
    }
    void display(String x){
        System.out.println(x);
    }

    public static void main(String[] args) {
        TypeOfPara typepara = new TypeOfPara();
        typepara.display(100);
        

    }
}
